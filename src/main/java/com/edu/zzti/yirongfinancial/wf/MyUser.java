package com.edu.zzti.yirongfinancial.wf;

public class MyUser {

	private String imei;
	private String name;
	private String pass;

	public MyUser() {

	}

	public String getImei() {
		return imei;
	}

	public void setImei(String imei) {
		this.imei = imei;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}

}
